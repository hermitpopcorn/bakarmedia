	<a name='akun'><h2>Map</h2></a>
	<ol>
		<li>
			Map ditandai dengan tiga warna: merah, biru, dan hijau. Berkas-berkas dalam map merah hanya dapat diakses oleh pemiliknya sendiri (map pribadi),
			berkas-berkas map biru dapat diakses oleh semua orang yang memiliki pranalanya (map tertutup).
			Pada map hijau, selain semua orang bisa mengakses berkas di dalamnya, map hijau memiliki pranala, membolehkan semua orang
			melihat semua isinya (map publik).
		</li>
		<li>
			Map dasar dan semua isinya bersifat tertutup. Ya, <a href='pengguna/pengaturan'>tentu saja anda dapat mengubah pengaturan ini</a>.
		</li>
	</ol>
