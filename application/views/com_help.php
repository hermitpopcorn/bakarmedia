			Jika masih bertubrukkan, sufiks "_BARU" akan terus ditambahkan sampai tidak bertubrukan.
			Nama berkas dibatas 256 karakter, jadi jika cukup panjang, duplikasi nama tetap bisa terjadi.
			Lagipula untuk apa anda memiliki berkas dengan nama sepanjang itu?
		</li>
